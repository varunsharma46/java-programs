public class switchcase {
    public static void main (String args[]){
        int number =  2;
        switch (number) {
            case 1: System.out.println("Mnago");
                break;
                case 2:System.out.println("Apple");
                break;
                case 3:System.out.println("Bnana");
                break;
                default:System.out.println("nothing");
                break;
        }
    
    
    }
}
